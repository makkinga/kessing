exports.getFirstName = function (gender, firstNameIndex) {
    let name

    // Male
    if (gender === 1) {
        name = maleFirstNames[firstNameIndex]
    }

    // Female
    if (gender === 3) {
        name = femaleFirstNames[firstNameIndex]
    }

    return name.charAt(0).toUpperCase() + name.slice(1)
}

exports.getLastName = function (lastNameIndex) {
    const name = lastNames[lastNameIndex]

    return name.charAt(0).toUpperCase() + name.slice(1)
}

exports.getFullName = function (gender, firstNameIndex, lastNameIndex) {
    return this.getFirstName(gender, firstNameIndex) + ' ' + this.getLastName(lastNameIndex)
}

exports.getRarity = function (rarityIndex) {
    return rarities[rarityIndex]
}

exports.getColor = function (rarityIndex) {
    return rarityColors[rarityIndex]
}

exports.getStaminaPercentage = function (stat) {
    return `${parseFloat(stat / 100).toFixed(2)}%`
}

